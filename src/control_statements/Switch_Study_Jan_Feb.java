package control_statements;

public class Switch_Study_Jan_Feb {

	public static void main(String[] args) 
	{
	//month declaration
		 String Month="May";
		
		switch (Month) 
		{
		case "Jan":System.out.println("this the 1st Month of Year");
		break;
		
		case "Feb":System.out.println("This is the 2nd Month of Year");
		break;
		
		case "March":System.out.println("This is the 3rd Month of Year");
		break;
		
		case "April":System.out.println("This is the 4th Month of Year");
		break;
		
		case "May":System.out.println("This is the 5th Month of Year");
		break;
		
		case "June":System.out.println("This is the 6th Month of Year");
		break;
		
		case "July":System.out.println("This is the 7th Month of Year");
		break;
		
		case "August":System.out.println("This is the 8th Month of Year");
		break;
		
		case "Sep":System.out.println("This is the 9th month of Year");
		break;
		
		case "Oct":System.out.println("This is the 10th month of Year");
		break;
		
		case "Nov":System.out.println("This is the 11th Month of Year");
		break;
		
		case "Dec":System.out.println("This is the 12th Month of Year");
		break;
		
		default:System.out.println("Please enter value between Jan-Dec");
		break;
		
		
		
		
		
		
		
		
		
		
		
		
		
		}
		
		
		
		
		
		
		
		
		
		
		}
		
		
		
		
		

	}


