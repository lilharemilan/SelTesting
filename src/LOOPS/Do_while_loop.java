package LOOPS;

public class Do_while_loop {

	public static void main(String[] args) 
	{
		//I want to print velocity 3 times
		 int i=1;
		 do
		 {
		System.out.println("Velocity");	//1,2,3-->4 
		i++;	 
		 }
		while(i<=3);
		 
		 //want to print 50-1 numbers
		 
		 int j=50;
		 do
		 {
			 System.out.println(j);//2,
			j--;//1,0 
		 }
		 while(j>=1);
		 
		 }
		 
		 
		 
		 

	}


